        <h2>Microsoft</h2>
        <p>Microsoft was founded by Bill Gates and Paul Allen on April 4, 1975, in Albuquerque, New Mexico. The company's original mission was to develop and sell a version of the BASIC programming language for the Altair 8800, an early personal computer. This marked the beginning of Microsoft's journey into the world of software.
            In 1980, Microsoft struck a deal with IBM to provide an operating system (OS) for their upcoming personal computer. Instead of creating an OS from scratch, Microsoft purchased a 86-DOS, an existing operating system, and modified it for the IBM PC. This operating system, known as MS-DOS, became a crucial element in the success of IBM's PC, and Microsoft retained the rights to license it to other PC manufacturers.
            The breakthrough moment for Microsoft came in 1985 with the release of Windows 1.0, a graphical extension for MS-DOS. It introduced a new era of computing with a graphical user interface, making it more user-friendly. However, it wasn't until Windows 3.0 in 1990 that Microsoft gained widespread success in the consumer market.
            Windows 95, released in—you guessed it—1995, was a major milestone. It featured a revolutionary user interface, a Start menu, and built-in support for networking. It became a huge commercial success and solidified Microsoft's dominance in the personal computer market.
            Bill Gates played a pivotal role in Microsoft's success, providing vision, leadership, and a keen business sense. He became the world's youngest self-made billionaire by the age of 31.
            In the late '90s, Microsoft faced antitrust lawsuits, accusing the company of engaging in anti-competitive practices to maintain its monopoly. Despite the legal challenges, Microsoft continued to evolve its products. Windows XP, released in 2001, became one of the most popular operating systems in history.
            Under Gates' leadership, Microsoft expanded its product range to include the Office suite, which includes applications like Word and Excel. The company also delved into the gaming industry with the launch of the Xbox console in 2001.
            In 2008, Bill Gates stepped down from his day-to-day role at Microsoft to focus on philanthropy. He and his then-wife, Melinda, established the Bill & Melinda Gates Foundation, one of the world's largest private charitable foundations.
            Though Bill Gates is no longer involved in the day-to-day operations of Microsoft, his legacy as a tech pioneer and philanthropist endures. The company he co-founded remains a major player in the tech industry, constantly innovating and shaping the future of computing.
        </p>
