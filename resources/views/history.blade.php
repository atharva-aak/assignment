        <h2>Philanthropy</h2>
        <p>In 2000, he and his then-wife Melinda established the Bill & Melinda Gates Foundation, with a focus on global health, education, and poverty alleviation. They've donated billions of dollars to initiatives like combating infectious diseases, improving access to education, and supporting agricultural development.
            One of their major initiatives is the fight against infectious diseases, particularly in developing countries. They've played a significant role in the efforts to eradicate polio, investing both funds and expertise. The Gates Foundation has also been actively involved in efforts to improve healthcare systems in developing nations.
            In education, the foundation has worked on initiatives to enhance educational opportunities and access, both in the United States and globally. They've supported the development of new teaching methods, technology integration, and scholarships.
            In 2019, Bill and Melinda Gates announced their divorce, but they emphasized their continued commitment to the foundation's work. It's a testament to their dedication to making a positive impact on the world, and their philanthropic efforts continue to be influential and expansive.</p>
